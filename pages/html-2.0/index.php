<h1>HTML 2.0 - An exploration!</h1>

<h2>What the World Wide Web Consortium Says</h2>

<p>
According to the W3C site, HTML 2.0:
</p>

<blockquote>
	<a href="http://www.w3.org/MarkUp/html-spec/">HTML 2.0</a> (<a href="http://www.rfc-editor.org/rfc/rfc1866.txt">RFC 1866</a>) was developed by the IETF's HTML Working Group, which closed in 1996. It set the standard for core HTML 	features based upon current practice in 1994. Note that with the release 	of <a href="http://www.ietf.org/rfc/rfc2854.txt">RFC 2854</a>, 	RFC 1866 has been obsoleted and its <a href="http://www.rfc-editor.org/rfc-index2.html">current status</a> is <strong>HISTORIC</strong>.
</blockquote>

<h2>Why do I find this interesting?</h2>

<pre>
			I really dig looking at these old
			documents. I'm writing this in 2001, and
			these were written only a few years ago,
			but it might as well have been written
			in the stone age. So much has changed.
</pre>


<p>Highlighted Arcana</p>
<ul>
	<li>References to quaint tags (some of which are dead, some not):
		<ul>
			<li>&lt;XMP&gt;</li>
			<li>&lt;LISTING&gt;</li>
			<li>&lt;PLAINTEXT&gt;</li>
			<li>&lt;CITE&gt;</li>
			<li>&lt;CODE&gt;</li>
			<li>&lt;KBD&gt;</li>
			<li>&lt;VAR&gt;</li>
			<li>&lt;TT&gt;</li>
		</ul>

</li>
	<LI>In the "Anchor A" area - a distinction is made between the HREF and URN - which is something lost in subsequent versions of html.</li>
	<li>References to "Fragment Identifiers" by which they mean in-page anchors - I find this language quaint and quite charming.</li>
	<li>width, height, and border for the &lt;IMG&gt; tag did not yet exist.</li>
	<li>No tables! No colors!</li>
	<li>No mention of font FACE, but some mentions of fonts  -- The Typical rendering for H1 -- for example is noted as:
			Bold, very-large font, centered. One or two blank lines
            above and below.</li>
</ul>

