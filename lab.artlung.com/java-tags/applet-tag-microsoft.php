APPLET Element | APPLET Object


 Places executable content on the page. 
 HTML Syntax

        &lt;APPLET
            ALIGN=ABSBOTTOM | ABSMIDDLE | BASELINE | BOTTOM
                | LEFT | MIDDLE | RIGHT | TEXTTOP | TOP
            ALT=text
            CLASS=classname
            CODE=filename
            CODEBASE=url
            DATAFLD=colname
            DATASRC=#ID
            HEIGHT=n
            HSPACE=n
            ID=value
            NAME=name
            SRC=url
            STYLE=css1-properties
            TITLE=text
            VSPACE=n
            WIDTH=n
            event = script
        &gt;

 Remarks

        This element is a block element. 

